
def loadImage(image, colorMap) -> list:
    result = []
    for x in range(0, len(image)):
        currentToAddTo = []
        for y in range(0, len(image[x])):
            currentToAddTo.append(
                colorMap[image[x][y]]
            )
        result.append(currentToAddTo)
    return result

